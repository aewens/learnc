#include <stdio.h>

int main(int argc, char * argv[])
{
    int one = 1;
    float half = 0.5f;
    double pi = 3.14159265;
    char q = "Q";
    char name[] = "Austin";
    
    printf("Int: %d", one);
    printf("Float: %f", half);
    printf("Double: %f", pi);
    printf("Char: %c", q);
    printf("String: %s", name);
    
    return 0;
}
