#include <stdio.h>

int main(int argc, char * argv[])
{
    int one = 1;
    float half = 0.5f;
    double pi = 3.14159265;
    char q = 'Q';
    char name[] = "Austin";
    
    printf("Int: %d\n", one);
    printf("Float: %f\n", half);
    printf("Double: %f\n", pi);
    printf("Char: %c\n", q);
    printf("String: %s\n", name);
    
    return 0;
}
